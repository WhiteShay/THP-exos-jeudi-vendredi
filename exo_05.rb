#Le #{} permet de réaliser des opérations sur les nombres dans des parties de texte.

puts "On va compter les heures de travail à THP" #Display la phrase entre ""
puts "Travail : #{10 * 5 * 11}" # Display le nombre d'heures passées à travailler sur THP
puts "En minutes ça fait : #{10 * 5* 11 * 60}" #Display le nb de minutes

puts "Et en secondes?" # Affiche la phrase

puts 10 * 5* 11 * 60 * 60 #Affiche le nb de secondes

puts "Est-ce que c'est vrai que 3/2 < 5-7 ?" # Affiche la phrase
puts 3 / 2 < 5 - 7 #Display une valeur boolénne (true/false) (comparaison de deux nombres)

puts "Ça fait combien 3 / 2 ? #{3 / 2}" # Display la phrase + la valeur
puts "Ça fait combien 5 - 7 ? #{5 - 7}" # Display la phrase + la valeur

puts "Ok, c'est faux alors !" # Affiche la phrase

puts "C'est drôle, ça, faisons en plus :" # Affiche la phrase

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" #Display la phrase + une valeur boolénne (true/false) (comparaison de deux nombres)
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" #Display la phrase + une valeur boolénne (true/false) (comparaison de deux nombres)
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" #Display la phrase + une valeur boolénne (true/false) (comparaison de deux nombres)